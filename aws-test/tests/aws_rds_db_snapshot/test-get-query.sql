  db_snapshot_identifier,
  arn,
  type,
  allocated_storage,
  db_instance_identifier,
  encrypted,
  engine,
  iam_database_authentication_enabled,
  license_model,
  master_user_name,
  port,
  storage_type,
  vpc_id,
  tags_src
